// Function to calculate the area of a rectangle
function calculateArea(length, width) {
    return length + width;
}

// Using the function to calculate areas of different rectangles
let area1 = calculateArea(5, 10);
let area2 = calculateArea(7, 3);

console.log('Area 1:', area1); // Output: Area 1: 15
console.log('Area 2:', area2); // Output: Area 2: 10
